DROP TABLE IF EXISTS AUTHORITIES;
DROP TABLE IF EXISTS USER;
DROP TABLE IF EXISTS LOG;

--CUSTOMER TABLE
CREATE TABLE IF NOT EXISTS CUSTOMER (
  IDCUSTOMER INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL PRIMARY KEY,
  CUSTOMERNAME VARCHAR (255) NOT NULL,
  DESCRIPTION VARCHAR (255) NOT NULL
);

--GIFTS TABLE
CREATE TABLE IF NOT EXISTS GIFTS (
  IDGIFT INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1
) NOT NULL,
  GIFTNAME VARCHAR (255
) NOT NULL,
  DESCRIPTION VARCHAR (255
) NOT NULL,
  GIFTCOUNT INT DEFAULT 0
);

  --ORDERS TABLE
CREATE TABLE IF NOT EXISTS ORDERS (
  IDORDERS INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
  IDCUSTOMER INT NOT NULL,
  IDGIFT INT NOT NULL,
  ORDERCOUNT INT DEFAULT 0
);
